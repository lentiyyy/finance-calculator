"""Генератор пояснительной записки курсового проекта.

Формирует docx-файл по требованиям ГОСТ из методических указаний:
- Times New Roman 12 pt, межстрочный 1.5
- Поля: левое 30 мм, правое 15 мм, верхнее/нижнее 20 мм
- Абзацный отступ 1.25 см
- Сквозная нумерация страниц (по центру вверху, кегль 11.5)
- Заголовки разделов нумерованные
"""

from docx import Document
from docx.shared import Pt, Cm, Mm
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING
from docx.enum.section import WD_SECTION
from docx.oxml.ns import qn, nsmap
from docx.oxml import OxmlElement


FONT_NAME = "Times New Roman"
BODY_SIZE = Pt(12)
PAGE_NUM_SIZE = Pt(11.5)
LINE_SPACING = 1.5
INDENT = Cm(1.25)


def set_run_font(run, size=BODY_SIZE, bold=False):
    run.font.name = FONT_NAME
    run.font.size = size
    run.bold = bold
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.append(rFonts)
    rFonts.set(qn('w:ascii'), FONT_NAME)
    rFonts.set(qn('w:hAnsi'), FONT_NAME)
    rFonts.set(qn('w:cs'), FONT_NAME)
    rFonts.set(qn('w:eastAsia'), FONT_NAME)


def add_paragraph(doc, text, *, align=WD_ALIGN_PARAGRAPH.JUSTIFY, indent=True,
                  bold=False, size=BODY_SIZE, space_before=0, space_after=0):
    p = doc.add_paragraph()
    p.alignment = align
    pf = p.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.first_line_indent = INDENT if indent else Cm(0)
    pf.space_before = Pt(space_before)
    pf.space_after = Pt(space_after)
    run = p.add_run(text)
    set_run_font(run, size=size, bold=bold)
    return p


def add_heading_1(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    pf = p.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.first_line_indent = Cm(0)
    pf.space_before = Pt(12)
    pf.space_after = Pt(12)
    pf.page_break_before = True
    run = p.add_run(text.upper())
    set_run_font(run, bold=True)
    p.style = doc.styles['Heading 1']
    p.style.font.name = FONT_NAME
    p.style.font.size = BODY_SIZE
    p.style.font.bold = True
    p.style.font.color.rgb = None
    return p


